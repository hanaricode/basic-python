# String to Integer
num_str = "95"
num_int = int(num_str)
print("String to integer:", num_int)
print("Type:", type(num_int))
print()

# String to Float
num_str2 = "3.14"
num_float = float(num_str2)
print("String to float:", num_float)
print("Type:", type(num_float))
print()

