# division
num3 = float(input("Masuka angka yang ingin dibagi: "))
num4 = float(input("Masuka angka pembagi: "))
if num4 == 0:
  print("error: tidak bisa dibagi dengan nol")
else:
  div_result = num3 / num4
  print(f"divison: {num3} / {num4} = {div_result}")


