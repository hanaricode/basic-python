print("1. Hamburger")
print("2. Cheeseburger")
print("3. Kentang Goreng")
print("4. Air Putih")
print("0. Keluar")

while True:
    try:
        pilihan = int(input("\nMasukkan nomor menu: "))

        match pilihan:
            case 1:
                print("Anda memilih Hamburger — Apakah ada tambahan lagi?")
            case 2:
                print("Anda memilih Cheeseburger — Apakah ada tambahan lagi?")
            case 3:
                print("Anda memilih Kentang Goreng — Apakah ada tambahan lagi?")
            case 4:
                print("Anda memilih Air putih - Apakah ada tambahan lagi?")
            case 0:
                print("Terima kasih, telah membeli disini.")
                break
            case _:
                print("Pilihan tidak valid, silakan coba lagi.")
                
    except ValueError:
        print("Input harus berupa angka, bukan huruf!")
